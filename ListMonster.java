    void displayMonster(String nama) {
        Monster temp = head;
        while(temp != null) {
            if(temp.name.equals(nama)) break;
            else temp = temp.next;
        }
        if(temp == null) System.out.println("Monster not found!");
        else {
            System.out.println(temp.name);
            System.out.println(temp.model);
        }
    }

